type EndpointMethod = "DELETE" | "GET" | "PATCH" | "POST"
type EndpointAudience = "admin" | "public_webhook"
type EndpointTag = "Config Objects" | "Plugins" | "Marketplaces" | "Connectors" | "GitHub" | "Webhooks"

type EndpointContract = {
  audience: EndpointAudience
  description: string
  method: EndpointMethod
  path: string
  request?: {
    body?: z.ZodTypeAny
    headers?: z.ZodTypeAny
    params?: z.ZodTypeAny
    query?: z.ZodTypeAny
  }
  response: {
    description: string
    schema?: z.ZodTypeAny
    status: 200 | 201 | 202 | 204 | 401
  }
  tag: EndpointTag
}

type DeferredEndpointContract = {
  description: string
  method: EndpointMethod
  path: string
  reason: string
  tag: EndpointTag
}

